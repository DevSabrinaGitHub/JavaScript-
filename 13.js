//funciones PARAMETROS Y ARGUMENTOS 


function sumar(n1, n2){   //n1 y n2 son parametros actuan como variables
    console.log (n1+n2);
}

sumar(10, 10);   //los valores estos son argumentos


//puedo llamar muchas veces las funciones y agregar argumentos
sumar(5,5);
sumar(1,1); //etc




