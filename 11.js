//FUNCIONES 

//funcion declarada
function sumar(){  //ya es llamada antes de escribir el codigo 
    console.log (6+6);
};

sumar(); //puedo poner esto arriba de la funcion, puedo llamarlas antes.





//funcion expresada :  Deben ser declaradas antes de ser utilizadas.
const suma2= function(){ //esto es lo mismo que la funcon sumar
    console.log (10+10); 
}
suma2(); //si o si se llaman despues de escribir el codigo. 

/*las funciones declaradas y expresadas ambas formas cumplen la misma función y son igualmente válidas. */


